/*
 * Programa para almacena en variables:
 * -Una temperatura (ªC).
 * -Numero de paginas de un libro.
 * -Numero de habitantes de una ciudad importante.
 * -La población mundial.
 */

public class ejercicio0205 {
    public static void main(String[] args) {
        
        byte temperatura = -5;
        short paginas = 364;
        int habitantes = 120000;
        long poblacion = 309293827;


        System.out.println("La temperatura es de " + temperatura + "ºC.");
        System.out.println("El numero de paginas es de " + paginas);
        System.out.println("El numero de habitantes es de " + habitantes);
        System.out.println("La poblacion mundial es de " + poblacion + " habitantes.");
    }
}
